class NumericDataTypes {
    static void main(String [] args) {
        //Notice that when using datatype, identifier def is not required

        int x = 5
        long y = 100l
        float a = 10.56f
        double b = 10.5e40
        BigInteger bi = 30g
        BigDecimal bd = 3.5g

        println(x)
        println(y)
        println(a)
        println(b)
        println(bi)
        println(bd)
    }
}
